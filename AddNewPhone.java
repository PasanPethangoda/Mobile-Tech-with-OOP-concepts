    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed

           try{
            //Open DB connecion
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/techmobile", "root","");
            
           //sql query to Add new phone data into DB
            String sql = "INSERT INTO phonedetails (phoneBrand, phoneName, phoneCategory) VALUES(?,?,?)";
           //Used to execute parameterized SQL queries
            PreparedStatement pstmt = conn.prepareStatement(sql);
            
            //Get data to Textfield 
            pstmt.setString(1, txtBrand.getText());
            pstmt.setString(2, txtName.getText());
            pstmt.setString(3, txtCategory.getText());
            
           //update the sql table
            pstmt.executeUpdate();
             //Display Successful Message
            JOptionPane.showMessageDialog(this, "Add" + " " + txtBrand.getText() + " " + txtName.getText() + " " + "Phone Details Successfully","SUCCESS", JOptionPane.INFORMATION_MESSAGE);
                        
            //close the connection
            conn.close();
            
            //clear text field
            clearField();
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

