    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        //try bloack
        try{
            //Open DB connection
            Class.forName("com.mysql.jdbc.Driver");
            Connection conn = DriverManager.getConnection("jdbc:mysql://localhost:3306/techmobile", "root","");
            
            //Query for Add data in DB table
            String sql = "INSERT INTO cashier (Cname, Cpassword) VALUES(?,?)";
            PreparedStatement pstmt = conn.prepareStatement(sql);
            
            //Get text fields data from user name and password
            pstmt.setString(1, txtName.getText());
            pstmt.setString(2,new String(txtPw.getPassword()));
             
           //update the fields
            pstmt.executeUpdate();
            
            //Successfully message
            JOptionPane.showMessageDialog(this, "Successfully Create New User Account To"+ " " + txtName.getText() + " " , "SUCCESS", JOptionPane.INFORMATION_MESSAGE);
            
            //close connection
            conn.close();   
            
            //clear fields
            clearField();
            
           // catch block 
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
        
    }//GEN-LAST:event_btnSaveActionPerformed

